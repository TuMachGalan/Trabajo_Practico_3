    private void jBtnVerificarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBtnVerificarMouseClicked
        // TODO add your handling code here:
        //Agregado de Pantalla
        String usuario = jTextUsuario.getText(); 
        if(usuario.isEmpty()){
            JOptionPane.showMessageDialog(null, "Porfavor Ingrese un Correo", "Campo Vacio",JOptionPane.WARNING_MESSAGE);
        }else{
            JOptionPane.showConfirmDialog(null, "Usuario Verificad: " + usuario,"Verificacion Exitosa",JOptionPane.INFORMATION_MESSAGE);
        }
        
        
    }//GEN-LAST:event_jBtnVerificarMouseClicked

